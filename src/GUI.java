  private void buttonLibrosFormularioQuitarmouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buttonLibrosFormularioQuitarmouseReleased
    try {
      String tituloBuscado = jComboBox2.getSelectedItem().toString();
      Libro buscado = new Libro ("", 0, "", 0);
      for (Libro libro : this.biblioteca.getLibros()) {
        if (tituloBuscado.equals(libro.getTitulo())) {
          buscado = libro;
        }
      }
      this.biblioteca.quitarLibro(buscado);
      this.buttonLibrosQuitarmouseReleased(evt);
    } catch (NullPointerException e) {
      javax.swing.JOptionPane.showMessageDialog(null, "Debe seleccionar un libro.",
        "Error al quitar", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
  }//GEN-LAST:event_buttonLibrosFormularioQuitarmouseReleased

