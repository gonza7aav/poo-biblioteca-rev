  private void buttonPrestamosFormularioRealizarmouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buttonPrestamosFormularioRealizarmouseReleased
    try {
      String tituloBuscado = jComboBox6.getSelectedItem().toString();
      Libro libroBuscado = new Libro ("", 0, "", 0);
      for (Libro libro : this.biblioteca.getLibros()) {
        if (tituloBuscado.equals(libro.getTitulo())) {
          libroBuscado = libro;
        }
      }
      
      int dni = Integer.valueOf(jComboBox5.getSelectedItem().toString());
      Socio socioBuscado = null;
      for (Socio socio : this.biblioteca.getSocios()) {
        if (dni == socio.getDNI()) {
          socioBuscado = socio;
        }
      }
      
      if (this.biblioteca.realizarPrestamo(Calendar.getInstance(), socioBuscado, libroBuscado)) {
        javax.swing.JOptionPane.showMessageDialog(null, "Se ha realizado el prestamo con exito.",
          "Informacion", javax.swing.JOptionPane.PLAIN_MESSAGE);
      } else if (!socioBuscado.puedePedir()) {
        javax.swing.JOptionPane.showMessageDialog(null, "El socio no puede pedir prestamos.",
          "Error al prestar", javax.swing.JOptionPane.ERROR_MESSAGE);
      } else {
        javax.swing.JOptionPane.showMessageDialog(null, "El libro se encuentra prestado.",
          "Error al prestar", javax.swing.JOptionPane.ERROR_MESSAGE);
      }
    } catch (NullPointerException e) {
      javax.swing.JOptionPane.showMessageDialog(null, "Debe seleccionar un libro y un socio.",
        "Error al prestar", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
  }//GEN-LAST:event_buttonPrestamosFormularioRealizarmouseReleased

